package net.stenyalang.parsing.impl.header.type;

/**
 * The enum Class type.
 */
public enum ClassType {

    /**
     * Static class type.
     */
    STATIC,
    /**
     * Dynamic class type.
     */
    DYNAMIC,

    /**
     * Enum class type.
     */
    ENUM,
    /**
     * Object class type.
     */
    OBJECT,

    /**
     * Super class type.
     */
    SUPER;

}
